package com.juaracoding.main.model;

public class Bonus {
	
	private int ID_Pekerja;
	private String Bonus_Date;
	private Double Bonus_Amount;
	
	
	public int getID_Pekerja() {
		return ID_Pekerja;
	}
	public void setID_Pekerja(int iD_Pekerja) {
		ID_Pekerja = iD_Pekerja;
	}
	public String getBonus_Date() {
		return Bonus_Date;
	}
	public void setBonus_Date(String bonus_Date) {
		Bonus_Date = bonus_Date;
	}
	public Double getBonus_Amount() {
		return Bonus_Amount;
	}
	public void setBonus_Amount(Double bonus_Amount) {
		Bonus_Amount = bonus_Amount;
	}
	
	
	

}
